#include <iostream>
using namespace std;

int main() {

	cout << "(5 & 7)  : " << (5 & 7) << endl;
	cout << "(5 ^ 7)  : " << (5 ^ 7) << endl;
	cout << "(5 ^ 10)  : " << (5 ^ 10) << endl;
	cout << "(5 | 7)  : " << (5 | 7) << endl;
	cout << "(1 << 3)  : " << (1 << 3) << endl;
	cout << "(8 >> 3)  : " << (8 >> 3) << endl;

	return 0;
}
















