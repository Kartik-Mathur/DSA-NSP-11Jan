#include <iostream>
using namespace std;

int main() {

	/*
	1. Initialisation
	2. Condition Check
	3. Updation
	*/

	int i, n;

	cin >> n; // Take input of n from user

	i = 1; // Initialisation

	while (i <= n) { // Condition Check

		cout << i << ' ';

		i = i + 1; // Updation
	}



	return 0;
}
















